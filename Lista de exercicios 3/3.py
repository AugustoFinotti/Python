# 3- Faca um algoritmo utilizando o comando while que mostra uma contagem regressiva na tela,
# iniciando em 10 e terminando em 0. Mostrar uma mensagem “FIM!” apÓs a contagem.

# WHILE
n = 10
while( n >= 0 ):
    print(n)
    n -= 1
print("FIM!")